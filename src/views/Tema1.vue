              tr
                td Blanquear.
                td Batir huevos o yemas con azúcar hasta obtener una mezcla espumosa, de color claro y que aumenta significativamente su volumen. Este proceso incorpora aire y disuelve parcialmente el azúcar, siendo fundamental en la elaboración de bizcochos y algunas cremas.
              tr
                td Caramelizar.
                td Calentar azúcar hasta que se funde y adquiere color ámbar, desarrollando sabores complejos y característicos. El proceso debe controlarse cuidadosamente, pues el azúcar puede pasar rápidamente de caramelizada a quemada.
